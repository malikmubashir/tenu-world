/**
 * Check if a French postal code is in zone tendue.
 * Returns false for non-French or invalid codes.
 */
export function isZoneTendue(postalCode: string): boolean {
  const cleaned = postalCode.trim().replace(/\s/g, "");
  if (!/^\d{5}$/.test(cleaned)) return false;
  return ZONE_TENDUE_POSTAL_CODES.has(cleaned);
}

/**
 * Extract postal code from a French address string.
 * Looks for a 5-digit group that starts with a valid département prefix.
 */
export function extractPostalCode(address: string): string | null {
  const match = address.match(/\b(\d{5})\b/);
  if (!match) return null;
  const code = match[1];
  // Basic sanity: French postal codes are 01xxx to 98xxx
  const dept = parseInt(code.substring(0, 2), 10);
  if (dept < 1 || dept > 98) return null;
  return code;
}

/**
 * Determine notice period based on zone tendue status and furnished flag.
 * - Zone tendue, unfurnished: 1 month
 * - Zone tendue, furnished: 1 month
 * - Non-tendue, unfurnished: 3 months
 * - Non-tendue, furnished: 1 month (always 1 for meublé per Loi ALUR)
 */
export function getNoticePeriodMonths(
  zoneTendue: boolean,
  furnished: boolean,
): number {
  if (furnished) return 1; // Meublé is always 1 month (Article 25-8 Loi 89-462)
  return zoneTendue ? 1 : 3;
}

/**
 * Get deposit return deadline in months.
 * - État des lieux conforme (no deductions): 1 month
 * - Deductions: 2 months
 * Both measured from key return date.
 * Ref: Article 22 Loi 89-462
 */
export function getDepositReturnDeadlineMonths(hasDeductions: boolean): number {
  return hasDeductions ? 2 : 1;
}

/**
 * Get département number from postal code.
 * Handles Corsica (20) and DOM-TOM (97x, 98x).
 */
export function getDepartement(postalCode: string): string | null {
  const cleaned = postalCode.trim().replace(/\s/g, "");
  if (!/^\d{5}$/.test(cleaned)) return null;
  // DOM-TOM: first 3 digits
  if (cleaned.startsWith("97") || cleaned.startsWith("98")) {
    return cleaned.substring(0, 3);
  }
  // Corse: 20 → 2A or 2B (simplified: return "20")
  return cleaned.substring(0, 2);
}
